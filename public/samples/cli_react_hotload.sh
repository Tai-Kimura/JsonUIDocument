# Start HotLoader development environment
rjui hotload
rjui hotload listen           # explicit start command

# Check HotLoader status
rjui hotload status

# Stop HotLoader
rjui hotload stop

# HotLoader features:
# - Real-time JSON layout updates
# - Automatic rebuild on file changes
# - Process management with PID tracking
# - Graceful shutdown with SIGTERM handling
# - Works alongside Next.js dev server
