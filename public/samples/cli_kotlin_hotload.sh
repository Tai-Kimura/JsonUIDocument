# Start HotLoader server for real-time updates
kjui hotload start
kjui hotload listen           # alias

# Stop HotLoader server
kjui hotload stop

# Check HotLoader status
kjui hotload status

# HotLoader enables:
# - Real-time UI updates on connected Android device
# - Instant preview of JSON layout changes
# - WebSocket-based communication (port 8081)
# - IP address auto-detection for device connection
# - Watches Layouts/ and Styles/ directories
